package Test.Day_2;

public class DataType {
    public static void main(String[] args) {
        //Integer

        //long
        long numberLong = 10000;
        System.out.println("numberLong : " + numberLong);

        //int
        int numberInt = 10;
        System.out.println("numberInt : " + numberInt);

        //short
        short numberShort = 1;
        System.out.println("numberShort : " + numberShort);

        //byte
        byte numberByte = 0;
        System.out.println("numberByte : " + numberByte);

        //Floating point
        //Float

        float numberFloat = 23454.5f;
        System.out.println("numberFloat : " + numberFloat);

        double numberDouble = 100.0;
        System.out.println("numberDouble : " + numberDouble);

        //Boolean

        boolean statu_1 = true;
        System.out.println("statu_1 : " + statu_1);

        boolean statu_2 = false;
        System.out.println("statu_2 : " + statu_2);


        //Character
        char text = 'A';
        System.out.println("text : " + text);

        //String
        String text_2 = "hello world!";
        System.out.println("text_2 : " + text_2);











    }
}
